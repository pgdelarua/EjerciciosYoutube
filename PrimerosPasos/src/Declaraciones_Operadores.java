
public class Declaraciones_Operadores {

	public static void main(String[] args) {
		
		int a=5; //declaración en la misma línea
		int b;
		b=7;	//declaración en dos líneas
		int c=b*a; //empleo de primer operador (*/-+..) si dividimos int pasa a ser double
		c++;	//me incrementa en uno el valor de c
		c+=6;	//me incrementa en 6 desde el valor incrementado c++
		c-=17;	//me resta uno el resultado obtenido hasta el paso anterior (para invalidar variables se puede hacer de ellas un comentario
		
		System.out.println (c);

	}

}
